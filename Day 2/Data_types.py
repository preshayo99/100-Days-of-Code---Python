#subscripting
print('Hello'[0])  # Prints the first letter of the string 'Hello'

# string concatenation
print('123' + '345')  # Concatenates two strings '123' and '345' to form '123345'

#integer = whole number
print(123 + 345)  # Adds two integers 123 and 345 to get 468

#larger integers
print(123_456_789)  # Adds two large integers with underscores for readability as coders

# float = decimal number
print(3.14159)  # Prints the float value of pi

# boolean = True or False
print(True)  # Prints the boolean value True
print(False)  # Prints the boolean value False